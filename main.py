def rps(p1, p2):
    if p1 == p2:
        return "Draw!"
    elif (p1 == 'rock' and p2 == 'scissors') or \
         (p1 == 'scissors' and p2 == 'paper') or \
         (p1 == 'paper' and p2 == 'rock'):
        return "Player 1 won!"
    else:
        return "Player 2 won!"


def no_space(x):
    lst = x.split(' ')
    string = ''
    for i in lst:
        string += i
    return string


def reverse(string):
    list = []
    str = ''
    for i in string:
        list.append(i)
    for j in list[::-1]:
        str += j
    return str


